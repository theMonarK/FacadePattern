    private void cdRadioBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cdRadioBtnActionPerformed
        this.type = HomeTheaterFacade.MediaType_Music;
        NewDiskDialog ndd = new NewDiskDialog(this,true);
        ndd.setMaterial("Record");
        ndd.setName("Artist");
    }//GEN-LAST:event_cdRadioBtnActionPerformed

    private void playBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playBtnActionPerformed
        switch (this.type) {
            case HomeTheaterFacade.MediaType_Music:
                this.feedbackLabel.setText(this.htf.listenCD());
                break;
            case HomeTheaterFacade.MediaType_Movie:
                this.feedbackLabel.setText(this.htf.watchMovie());
                break;
            case HomeTheaterFacade.MediaType_Tuner:
                this.feedbackLabel.setText(this.htf.listenRadio());
                break;
            default:
                break;
        }
    }//GEN-LAST:event_playBtnActionPerformed

