    private void pauseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pauseBtnActionPerformed
        switch (this.type) {
            case HomeTheaterFacade.MediaType_Music:
                this.feedbackLabel.setText(this.htf.pauseCD());
                break;
            case HomeTheaterFacade.MediaType_Movie:
                this.feedbackLabel.setText(this.htf.pauseMovie());
                break;
            default:
                break;
        }
    }//GEN-LAST:event_pauseBtnActionPerformed

    private void stopBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stopBtnActionPerformed
        switch (this.type) {
            case HomeTheaterFacade.MediaType_Music:
                this.feedbackLabel.setText(this.htf.endCD());
                break;
            case HomeTheaterFacade.MediaType_Movie:
                this.feedbackLabel.setText(this.htf.endMovie());
                break;
            case HomeTheaterFacade.MediaType_Tuner:
                this.feedbackLabel.setText(this.htf.endTuner());
                break;
            default:
                break;
        }
    }//GEN-LAST:event_stopBtnActionPerformed

